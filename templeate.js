function fullName (firstName, lastName){
    return `${firstName} ${lastName}`;
}
const result = fullName('jahid', 'Hassan');
console.log(result);

function jointString(firstLine, secondLine, thirdLine){
    return `${firstLine} ${secondLine} ${thirdLine}`;
}

const result2 = jointString ('This is jahid', 'I want to be a Programmer', 'I am computer Science Student');
console.log(result2);